package models;

import java.util.Date;

/**
 *
 * @author user
 */
public class User {
    
    
    
    int id ;
    String name;
    String birth;
    String adress ,email ;

    public User(int id, String name, String birth, String adress, String email) {
        this.id = id;
        this.name = name;
        this.birth = birth;
        this.adress = adress;
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBirth() {
        return birth;
    }

    public void setBirth(String birth) {
        this.birth = birth;
    }

    public String getAdress() {
        return adress;
    }

    public void setAdress(String adress) {
        this.adress = adress;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    
    
    
    
    
    
}
